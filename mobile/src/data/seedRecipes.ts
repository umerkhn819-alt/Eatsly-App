export function getRecipeImage(recipe: RecipePublic): string {
  const u = recipe.imageUrl?.trim();
  if (u) return u;
  for (const tag of recipe.tags) {
    if (RECIPE_TAG_IMAGES[tag]) return RECIPE_TAG_IMAGES[tag];
  }
  return "https://images.unsplash.com/photo-1504674900247-0877df9cc836?w=600";
}

